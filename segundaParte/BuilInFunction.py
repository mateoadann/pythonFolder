#Funciones buil-in

numeros =[1, 2, 5 ,6]

#Encontrando el número mayor en una lista
numero_mas_alto = max(numeros)
print(numero_mas_alto)


#Encontrando el número mas chico de la lista
numero_mas_bajo = min(numeros)
print(numero_mas_bajo)


#Redondeando a 6 decimales
numero = round(6.2326467345246, 2)
print(numero)

#Retorn False -> 0, vacio, False, ninguno
resultado = bool('ninguno')
print(resultado)

#Retorna True, si todos los valores son verdaderos
resultado_all = all(['asasdf',142,'fdhsfhs'])
print(resultado_all)

#La suma de todo
suma_total = sum(numeros)
print(suma_total)




